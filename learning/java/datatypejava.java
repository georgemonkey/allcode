package JavaPractice;

public class datatypejava {
    public static void main(String[] args) {
        byte num1;
        num1 = 111;
        System.out.println(num1);
        //byte can hold whole number from -128 to 127
        short num2 = 12345;
        //short can hold -32768 to 32767
        int num3 = 123456;
        //-2,147,483,648 to 2,147,483,647
        long num4 = 123456789L;
        //-9,223,372,036,854,775,808 to 9,223,372,036,854,775,802
        double num5 = -42937737.9d;
        //15 decimal digits 8 bytes of memory
        float num6 = 19.98f;
        //can hold up to 6 to 7 decimal digits
        boolean b = false;
        //holds either true or false
        char ch = '@';
        //holds one character
    }
}
